import mongoose from 'mongoose';

const hrContactSchema = new mongoose.Schema({
    categoryId: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'Category',
        required: true
    },
    name: {
        type: String,
        required: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        trim: true
    },
    phone: {
        type: String,
        trim: true
    },
    company: {
        type: String,
        trim: true
    },
    designation: {
        type: String,
        trim: true
    }
}, { timestamps: true });

export default mongoose.model('HrContact', hrContactSchema);
